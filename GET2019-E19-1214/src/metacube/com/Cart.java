package metacube.com;

public class Cart {
	
	String name;
	int quantity;
	double price;
	double totalAmount;
	public Cart(String name, int quantity, double price, double totalAmount) {
		super();
		this.name = name;
		this.quantity = quantity;
		this.price = price;
		this.totalAmount = totalAmount;
	}
	
	
}
